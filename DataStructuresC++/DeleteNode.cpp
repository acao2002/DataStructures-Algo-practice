SinglyLinkedListNode* deleteNode(SinglyLinkedListNode* head, int position) {
    SinglyLinkedListNode* p = head;
    if (position == 0) {
        head = head->next;
    }
    else{
        for(int i =0; i< position -1;i++){
            p = p->next;
        }
        p->next = p->next->next;
    }
    return head ;

}